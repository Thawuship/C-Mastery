// Explicit Conversion

// Declare a long variable and assign a value
long myLong = 123233453454;
Console.WriteLine(myLong);

// Convert the long variable to an int using explicit casting
int myInt = (int)myLong;
Console.WriteLine(myInt);

// Declare a double variable and assign a value
double myDouble = 234.234234242;
Console.WriteLine(myDouble);

// Convert the double variable to a float using explicit casting
float myFloat = (float)myDouble;
Console.WriteLine(myFloat);

// Convert the double variable to an int using explicit casting
int myInts = (int)(myDouble);
Console.WriteLine(myInts);


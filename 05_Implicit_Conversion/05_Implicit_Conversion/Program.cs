// This code demonstrates implicit type conversion in C#.
// Implicit conversions are automatically handled by the compiler when there is no risk of data loss.

// An integer variable, which can hold values from -2,147,483,648 to 2,147,483,647
int myInt = 1111111111;

// A double precision floating point variable, which can handle very large and very small numbers with fractional components
double myDouble = 11111111111111111111.111111111111111111;

// A long integer variable, which can hold larger integer values compared to int
long myLong = 1111111111111111111;

// A single precision floating point variable, which uses less memory than double but has less precision
float myFloat = 11111.1111f;

